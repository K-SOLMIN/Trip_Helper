function normalizeCountryCode(value) {
  const raw = String(value || '').trim();
  if (!raw) return '';
  const upper = raw.toUpperCase();
  return HOTEL_COORDS[upper] ? upper : COUNTRY_ALIASES[raw] || '';
}

function getHotelCoords(value) {
  const code = normalizeCountryCode(value);
  return code ? { code, ...HOTEL_COORDS[code] } : null;
}

